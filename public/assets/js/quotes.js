//global math functions
function getRandomInt(min, max) {
	return Math.floor(Math.random() * (max - min + 1)) + min;
}

$(document).ready(function() {
	$('.excerpt').each(function(){
		if(!$(this).text().length) {
			var randomQuote = quotes[getRandomInt(0, quotes.length - 1)];
			$(this).html('<div class="no-excerpt alert alert-primary"><p><em><strong>WELP</strong>. This article has no excerpt, so here\'s a random video game quote:</em></p><blockquote class="blockquote text-center"><p class="mb-0">&ldquo;' + randomQuote.excerpt + '&rdquo;</p><footer class="blockquote-footer">From  <cite>' + randomQuote.source + '</cite></footer></blockquote></div>');
		}
	});
});